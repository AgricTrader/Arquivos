
public class Unidade_Federacao {
	
	private String uf_id;
	
	private String desc_uf;

	public Unidade_Federacao(String uf_id, String desc_uf) {
		super();
		this.uf_id = uf_id;
		this.desc_uf = desc_uf;
	}

	public String getUf_id() {
		return uf_id;
	}

	public void setUf_id(String uf_id) {
		this.uf_id = uf_id;
	}

	public String getDesc_uf() {
		return desc_uf;
	}

	public void setDesc_uf(String desc_uf) {
		this.desc_uf = desc_uf;
	}
	
	

}
